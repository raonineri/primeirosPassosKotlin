package varibles

fun main(){

    /*
        Byte: 1 byte - 8 bits
        Short: 2 bytes - 16 bits
        => Int: 4 bytes - 32 bits
        => long: 8 bytes - 64 bits
     */

    val i1 = 10
    val i2: Int = 10
    val i3: Int

    val l1: Long
    // o L ao final do número força que a variavel seja reconhecida como o típo long
    val l2 = 10L
}